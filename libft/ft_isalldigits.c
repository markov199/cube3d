#include "libft.h"

int	ft_isalldigits(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (0);
	while (s[i] == ' ' || s[i] == '\t')
		i++;
	while (s[i] != '\0')
	{
		if (!(s[i] >= '0' && s[i] <= '9'))
			return (0);
		i++;
	}
	return (1);
}
