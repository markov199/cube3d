#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (!new)
		return ;
	if (!*lst)
		*lst = new;
	else
	{
		temp = *lst;
		while (temp->next != NULL)
			temp = temp->next;
		temp->next = new;
	}
}
/*
Funcao para adicionar um novo elemento 'new' no final da lista.
PARAMETROS: 1. The address of a pointer to the first link of a list;
2. The address of a pointer to the node to be added to the list.
*/