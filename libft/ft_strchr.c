#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	c = (char)c;
	while (s[i] != '\0' && c != s[i])
		s++;
	if (c == s[i])
		return ((char *)s);
	return (0);
}
