#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (0);
	while (lst->next != NULL)
		lst = lst->next;
	return (lst);
}
/*
Funcao que retorna o ultimo node da lista.
Paramentro lst eh o comeco da lista, o head.
Retorna o ultimo elemento da lista.
*/